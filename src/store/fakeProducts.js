export const getProductsBySearch = busca => {
  if (busca.length === 0) return [];
  const produto = Produtos.filter((item, index) => {
    let resultadoBusca = false;
    if (index <= 10)
      resultadoBusca =
        item.title.toLowerCase().indexOf(busca) >= 0 ? true : false;

    return resultadoBusca;
  });
  return produto;
};

export const getProductById = id => {
  const produto = Produtos.filter(item => {
    return parseInt(id) === item.id;
  });
  return produto[0];
};
